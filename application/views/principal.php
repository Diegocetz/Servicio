<div class="col-sm-8">

      <h2 class="card-title">Administración de sueldos y salarios</h2>
      <br>

      <p>Al momento de determinar el sueldo y salario, es importante considerar el salario mínimo y que el salario sea
        competitivo. También se puede colocar como irá incrementando el salario con el pasar del tiempo. </p>
      <p>Existen varios métodos para la asignación del sueldo y salario, siendo los siguientes: </p>
      <li>Método de ranking.</li>
      <p>Se comparan los puestos, clasificándolos de acuerdo a su importancia. La importancia se asigna según las
        responsabilidades del puesto. </p>
      <li>Método de evaluación por puntos.</li>
      <p>Los puestos se dividen en función de varios factores como la habilidad, el esfuerzo, la formación, el
        conocimiento, las responsabilidades, etc. Asignando puntos a cada factor.</p>
      <li>Método de comparación de factores.</li>
      <p>Clasifica los puestos mediante la comparación y analiza los puestos por factores comparables. Considera los
        factores a los que se les dará mayor o menor valor. Se utiliza para el staff, profesionales y gerenciales.</p>
      <li>Método de clasificación.</li>
      <p>Los grados de trabajo son establecidos por un comité autorizado según la dificultad o las habilidades que
        requieren.
        Al establecer los grados, cada puesto se clasifica según su grado o clase, fijando una tasa de salario diferente
        para cada grado.
      </p>
