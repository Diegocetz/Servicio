<h2>Misión</h2>
<p>Enunciado en el que una empresa comunica a los agentes externos e internos, el objeto, propósito o razón de ser una empresa</p>
<p>Para formular una misión se debe considerar las siguientes preguntas:</p>
<li>¿Quiénes somos?</li>
<li>¿Qué hacemos?</li>
<li>¿Porque lo hacemos?</li>
<li>¿Para que lo hacemos? </li>
<li>¿Para quien lo hacemos?</li>

<h2>Filosofía</h2>
<p>Para que la empresa tenga una identidad que defina la manera en la que se llevan a cabo los procesos internos y un estilo que distinga de la competencia, es necesario definir una filosofia empresarial</p>
<p>La filosofía es el conjunto de elementos que definen las practicas empresariales. Dentro de la filosofia se encuentra la misión, visión y los valores.</p>
<li>¿Hacia dónde nos dirigimos?</li>
<li>¿Dónde queremos estar en el futuro?</li>
<li>¿Cómo lo conseguiremos? </i>
   
<h2>Objetivos</h2>
<p>Resultados de una empresa que busca alcanza o los que pretende llegar en un periodo de tiempo definido, según sus recursos con los que dispone y la filofía de la empresa.</p>
<p>Es importante que al hacer un plan de negocios se realicen los objetivos ya que sin ellos no se pueden evaluar los resultados obtenidos</p>

<h2>Existen diferentes tipos de objetivos:</h2> 

<p>
    <strong>Objetivo general: </strong>Aspectos generales que la empresa busca conseguir, siendo el fin que buscan conseguir en base a su misión y visión.
</p>
<p>
    <strong>Objetivo específico: </strong>Conjunto de pautas que hacen posible el cumplimiento del objetivo general.
</p>
<p>
    <strong>Objetivo a corto plazo: </strong>Son menores a un año.
</p>
<p>
    <strong>Objetivo a mediano plazo: </strong>Van desde un año a 3 años.
</p>

<p>
    <strong> Objetivo a largo plazo: </strong>Desde 3 años en adelante.
</p>
            

       
<h2>Los objetivos deben de ser SMART:</h2>

<p>
    <strong>Medible: </strong>Los indicadores deben ser cuantificables, con criterios que permitan medir y evaluar su progreso. 
</p>
<p>

    <strong>Alcancable: </strong> Debe ser realista, tomando en cuenta las limitaciones que puede tener la empresa
</p>
<p>
    <strong>Relevante: </strong> Está alineado con otras metas  y tiene sentido con lo planteado en la empresa
</p>
<p>
    <strong>Tiempo: </strong> Establecer un marco de tiempo en base a una fech<li>fijando una fecha límite. 
</p>
 
<h2>Imagen corporativa</h2>
<p>La imagen corporativa es la percepción que tiene público hacia la empresa. Es el conjunto de creencias, ideas y sentimientos de los consumidores, dependiente de variables como los productos y servicios ofrecidos, la relación con los clientes y trabajadores y la sociedad.</p>

<p>Tener una buena imagen corporativa facilita que el público se indentifique con la marca, que la empresa sea facilmente reconocida y crear vinculo con el consumidor.</p>

<h2>Los elementos de la imagen corporativa son los siguientes:</h2>
<p>
  <strong>Nombre de la empresa: </strong>Debe ser breve, atractivo y diferenciador.
</p> 
<p>
  <strong>Eslogan: </strong>Resume la filosofía de la empresa de manera creativa
</p> 
<p>
  <strong>Logotipo y símbolos: </strong>Resume la esencia de la empresa con imágenes creativas y sencillas que van de acuerdo con los productos ofrecidos.
</p>
          
<p>
  <strong>Identidad cromática: </strong>Elección de colores utilizados para reforzar la imagen e identidad de la empresa
</p>        
          
